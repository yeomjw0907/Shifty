-- 방법 1: 이미 계정이 있다면 업데이트
UPDATE auth.users 
SET 
  email_confirmed_at = NOW(),
  updated_at = NOW()
WHERE email = 'test@shifty.com';

-- 방법 2: 없다면 아래 메시지를 따라주세요
SELECT 
  CASE 
    WHEN COUNT(*) > 0 THEN '✅ test@shifty.com 계정이 존재합니다!'
    ELSE '❌ 계정이 없습니다. 아래 단계를 따라주세요!'
  END as message
FROM auth.users
WHERE email = 'test@shifty.com';

